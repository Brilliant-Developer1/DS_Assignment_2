using namespace std;
class Node
{
public:
    int val;
    Node *next;

    Node(int val)
    {
        this->val = val;
        this->next = NULL;
    }
};
void insert_at_tail(Node *&head, Node *&tail, int value)
{
    Node *newNode = new Node(value);

    if (head == NULL)
    {
        head = newNode;
        tail = newNode;
        return;
    }
    tail->next = newNode;
    tail = newNode;
}
void descending_sort(Node *head)
{
    for (Node *i = head; i->next != NULL; i = i->next)
    {
        for (Node *j = i->next; j != NULL; j = j->next)
        {
            if (i->val < j->val)
            {
                swap(i->val, j->val);
            }
        }
    }
}
void print_middle(Node *head, int size)
{
    Node *temp = head;
    if (size == 1)
    {
        cout << temp->val;
    }
    else
    {
        for (int i = 1; i < size / 2; i++)
        {
            temp = temp->next;
        }
        if (size % 2 == 0)
        {
            cout << temp->val << " " << temp->next->val << endl;
        }
        else
        {
            cout << temp->next->val << endl;
        }
    }
}
int size(Node *head)
{
    Node *temp = head;
    int count = 0;
    while (temp != NULL)
    {
        count++;
        temp = temp->next;
    }
    return count;
}

int main()
{

    int value;
    Node *head = NULL;
    Node *tail = NULL;
    while (true)
    {
        cin >> value;
        if (value == -1)
            break;
        insert_at_tail(head, tail, value);
    }
    descending_sort(head);
    print_middle(head, size(head));
    return 0;
}