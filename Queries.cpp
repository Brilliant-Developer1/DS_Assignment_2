using namespace std;
class Node
{
public:
    long long int val;
    Node *next;

    Node(long long int val)
    {
        this->val = val;
        this->next = NULL;
    }
};
void insert_at_head(Node *&head, Node *&tail, long long int value)
{
    Node *newNode = new Node(value);

    if (head == NULL)
    {
        head = newNode;
        tail = newNode;
        return;
    }
    newNode->next = head;
    head = newNode;
}
void insert_tail(Node *&head, Node *&tail, long long int value)
{
    Node *newNode = new Node(value);
    if (head == NULL)
    {
        head = newNode;
        tail = newNode;
        return;
    }
    tail->next = newNode;
    tail = newNode;
}

int main()
{
    int input;
    Node *head = NULL;
    Node *tail = NULL;

    cin >> input;

    for (int i = 0; i < input; i++)
    {
        int x;
        long long int v;
        cin >> x;
        cin >> v;
        if (x == 0)
        {
            insert_at_head(head, tail, v);
        }
        else
        {
            insert_tail(head, tail, v);
        }
        cout << head->val << " " << tail->val << endl;
    }
    return 0;
}