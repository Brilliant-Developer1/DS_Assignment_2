using namespace std;
class Node
{
public:
    long long int val;
    Node *next;

    Node(long long int val)
    {
        this->val = val;
        this->next = NULL;
    }
};
void insert_tail(Node *&head, Node *&tail, long long int value)
{
    Node *newNode = new Node(value);
    if (head == NULL)
    {
        head = newNode;
        tail = newNode;
        return;
    }
    tail->next = newNode;
    tail = newNode;
}
void print_reverse(Node *head)
{
    if (head == NULL)
        return;
    print_reverse(head->next);
    cout << head->val << " ";
}
void print_original(Node *head)
{
    if (head == NULL)
        return;
    cout << head->val << " ";
    print_original(head->next);
}
int main()
{
    long long int input;
    Node *head = NULL;
    Node *tail = NULL;
    while (true)
    {
        cin >> input;
        if (input == -1)
            break;
        insert_tail(head, tail, input);
    }
    print_reverse(head);
    cout << endl;
    print_original(head);

    return 0;
}